# Operators:
"""
    1. == Checks whether the LHS is equal to RHS
    2. != Cheks whether the LHS is not equal to RHS
    3. > Checks whether the LHS is greater than RHS
    4. < Checks whether the RHS is greater than LHS
    5. >= Checks whether the LHS is greater than or equal to RHS
    6. <= Checks whether the RHS is lesser than or equal to LHS
    NOTE: They always return a boolean value i.e. true or false.
    They will be used along with the conditional statements.
"""

x,y = 77,88
print(x==y)
print(x!=y)
print(x>y)
print(x<y)
print(x>=y)
print(x<=y)